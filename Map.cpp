void Map::setMap()
{
	for (int i = 0; i < 16; ++i)
	{
		for (int j = 0; j < 16; j++)
		{
			if (tab[i][j] == 'W')
				board.setWall(j, i);
			else if (tab[i][j] == '.')
				board.removeWall(j, i);
			else if (tab[i][j] == 's')
				board.setSnakeHead(j, i);
		}
	}
}

